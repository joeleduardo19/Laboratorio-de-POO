public class DisOperacionesBasicas {
    
    public double suma(double a, double b){
        return a + b;
    }
    public double resta(double a, double b){
        return a - b;
    }
    public double division(double a, double b) {
        return a / b;
    }
    public double multi(double a, double b) {
        return a * b;
    }
}
